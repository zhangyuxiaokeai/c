# 转义字符以反斜杠“\”开头，后跟一个或几个字符。转义字符具有特定的含义，不同于字符原有的意义，故称转义字符，主要用来表示那些用一般字符不便于表示的控制代码。
# Python还允许在单引号前加r来表示单引号内部的字符串默认不转义。
# 如： print(r"d:\notebook.exe")
# open(r"d:\student.txt")
t = input("输入字符串")
d = x = 0
for ch in t:
    if ch.islower():   # 是小写
        x+=1
    elif ch.isupper():   # 是大写
        d+=1
print("大写字母个数：", d, "小写字母个数：", x)

# 集合是无序、可变的容器对象，所有元素放在一对大括号中，元素之间使用逗号分隔，同一个集合内的每个元素都是唯一的，不允许重复（利用这一特性，可将对象去重）。
# 集合中只能包含不可变类型的数据（数字、字符串、元组等），而不能包含列表、字典、集合等可变类型的数据，包含列表等可变类型数据的元组也不能作为集合的元素。
# 集合中的元素是无序的，元素存储顺序和添加顺序并不一致。
# 集合不支持使用下标直接访问特定位置上的元素，也不支持使用random中的choice()函数从集合中随机选取元素，但支持使用random模块中的sample()函数随机选取部分元素。
